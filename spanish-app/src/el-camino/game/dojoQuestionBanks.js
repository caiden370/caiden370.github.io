function getDistractors(cards, index, fieldIndex) {
  return [
    cards[(index + 5) % cards.length][fieldIndex],
    cards[(index + 9) % cards.length][fieldIndex]
  ];
}

function createChapterQuestions(chapter) {
  return chapter.cards.flatMap((card, index) => {
    const [spanish, english, sentence, sentenceEnglish] = card;
    const meaningDistractors = getDistractors(chapter.cards, index, 1);
    const sentenceDistractors = getDistractors(chapter.cards, index, 3);
    const spanishSentenceDistractors = getDistractors(chapter.cards, index, 2);
    const vocabularyQuestion = {
      type: "askChoice",
      prompt: `¿Qué significa "${spanish}"?`,
      options: [
        { label: english, correct: true },
        ...meaningDistractors.map((label) => ({ label, correct: false }))
      ]
    };

    const sentenceQuestion = index % 3 === 0
      ? {
          type: "askAudioChoice",
          prompt: "Escucha la frase y elige su significado.",
          audioText: sentence,
          options: [
            { label: sentenceEnglish, correct: true },
            ...sentenceDistractors.map((label) => ({ label, correct: false }))
          ]
        }
      : {
          type: "askChoice",
          prompt: `Elige la mejor traducción: ${sentenceEnglish}`,
          options: [
            { label: sentence, correct: true },
            ...spanishSentenceDistractors.map((label) => ({ label, correct: false }))
          ]
        };

    return [vocabularyQuestion, sentenceQuestion];
  });
}

export function createDojoMenuAction() {
  return {
    type: "askChoice",
    shuffle: false,
    prompt: "Maestro del Dojo: ¿Qué capítulo quieres entrenar?",
    wrongMessage: "Selecciona un capítulo para comenzar.",
    options: chapterDrills.map((chapter) => ({
      label: chapter.label,
      correct: true,
      actions: [
        {
          type: "askBossQuiz",
          bossId: `dojo-${chapter.id}`,
          title: chapter.title,
          restartMessage: "Todavía no. Revisa el significado y vuelve a intentarlo.",
          questions: createChapterQuestions(chapter),
          actions: [
            {
              type: "showDialogue",
              speaker: "Maestro del Dojo",
              text: `Completaste las 30 preguntas de ${chapter.label}. Repite el entrenamiento cuando quieras.`
            }
          ]
        }
      ]
    }))
  };
}

export function getDojoQuestionCounts() {
  return Object.fromEntries(
    chapterDrills.map((chapter) => [chapter.id, createChapterQuestions(chapter).length])
  );
}
